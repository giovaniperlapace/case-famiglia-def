export function normalizeNationality(value: string | null | undefined): NationalityOption | null {
  if (!value) return null;

  const normalizedKey = normalizeNationalityKey(value);
  return (
    NATIONALITY_BY_NORMALIZED_KEY.get(normalizedKey) ??
    NATIONALITY_ALIASES.get(normalizedKey) ??
    null
  );
}
