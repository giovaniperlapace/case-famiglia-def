type NationalityOption = (typeof NATIONALITY_OPTIONS)[number];

export const NATIONALITY_SET = new Set<string>(NATIONALITY_OPTIONS);

function normalizeNationalityKey(value: string): string {
  return value
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[’'`]/g, " ")
    .replace(/&/g, " e ")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, " ")
    .trim();
}

const NATIONALITY_BY_NORMALIZED_KEY = new Map<string, NationalityOption>(
  NATIONALITY_OPTIONS.map((option) => [normalizeNationalityKey(option), option])
);

